bt('Spring Boot是由Pivotal团队推出的一个基于Spring框架的快速开发框架，它的设计理念是"约定优于配置"。传统的Spring项目在搭建过程中需要编写大量的XML配置文件，开发效率较低且容易出错。Spring Boot通过自动配置机制，能够根据项目中引入的依赖自动完成大部分配置工作，开发者只需要关注业务逻辑的实现即可。Spring Boot内嵌了Tomcat等Web服务器，可以将应用打包成一个独立的可执行JAR文件，无需额外部署Web容器，大大简化了应用的部署和运维工作。')
bt('在本系统中，Spring Boot主要承担以下职责：一是作为Web服务器，接收和处理前端发送的HTTP请求；二是通过Spring MVC提供RESTful风格的API接口；三是集成MyBatis-Plus数据持久层框架，完成与MySQL数据库的交互操作；四是集成Spring Data Redis，实现对Redis缓存的操作；五是通过Spring的依赖注入和面向切面编程等特性，实现业务逻辑的模块化组织。本系统使用的Spring Boot版本为2.7.x，配合Java 17运行环境。')

h2('2.2 MySQL数据库')
