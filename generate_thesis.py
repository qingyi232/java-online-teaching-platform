h3('5.1.1 用户注册与登录')
bt('用户注册功能允许新用户创建平台账号。在注册页面，用户需要填写用户名、密码、确认密码，并选择注册角色（学生或教师）。前端对输入数据进行实时校验，包括用户名长度检查、密码一致性验证等。校验通过后，前端将注册信息发送到后端的/api/auth/register接口。后端接收到注册请求后，首先检查用户名是否已被占用，如果用户名可用，则使用BCrypt算法对密码进行哈希加密后存储到数据库中。注册成功后跳转到登录页面。')
bt('用户登录功能采用JWT令牌机制。用户在登录页面输入用户名和密码后，前端将其发送到后端的/api/auth/login接口。后端通过用户名查询数据库中的用户记录，然后使用BCrypt的checkpw方法验证输入密码与数据库中存储的密码哈希是否匹配。验证通过后，后端生成一个包含用户ID、用户名和角色信息的JWT令牌返回给前端。前端将令牌和用户信息保存在Vuex状态管理器和浏览器的localStorage中，之后每次向后端发送API请求时，都会在请求头的Authorization字段中携带该令牌。后端通过拦截器验证令牌的有效性和用户身份。登录流程如图5-1所示。')
