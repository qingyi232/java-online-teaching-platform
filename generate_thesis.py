bt('在本系统中，MinIO主要用于存储教学资源文件，包括PDF课件、PPT演示文稿、Word文档、视频文件等。教师上传教学资源时，系统将文件通过MinIO的Java SDK上传到MinIO服务器中的指定存储桶中，同时在MySQL数据库中记录文件的存储路径等元数据信息。当学生需要下载资源时，系统调用MinIO的预签名URL功能，生成一个带有时效限制的下载链接，学生通过该链接直接从MinIO服务器下载文件，从而减轻应用服务器的负载压力。')

h2('2.5 Vue与Element UI前端技术')
bt('Vue.js是一个用于构建用户界面的渐进式JavaScript框架，由尤雨溪于2014年发布。Vue的核心思想是数据驱动和组件化。通过数据驱动机制，当数据发生变化时，视图会自动更新；通过组件化机制，可以将复杂的页面拆分成多个独立的、可复用的组件。Vue还提供了Vue Router路由管理和Vuex状态管理等配套工具，能够支撑单页面应用的开发需求。')
bt('Element UI是饿了么前端团队开源的一套基于Vue 2.0的桌面端组件库，它提供了按钮、表单、表格、对话框、导航菜单、分页器等几十种常用的UI组件。这些组件经过了良好的封装和样式设计，开发者可以通过简单的标签引用和属性配置来使用它们，大大提高了前端界面的开发效率。')
bt('在本系统的前端开发中，Vue.js负责整体的页面路由管理、用户状态管理和组件的组织与渲染。Vuex被用来管理全局的用户登录状态、角色信息等共享数据。Axios库被用来向后端API发送HTTP请求并处理响应。Element UI则被用来构建各个功能页面的用户界面。前端还集成了ECharts图表库，用于在统计分析页面中绘制浏览趋势折线图、下载趋势折线图、分类浏览分布饼图和下载量排行柱状图等可视化图表。')

