bt('在本系统中，MySQL用于存储所有的结构化业务数据，包括用户信息、教学资源元数据、资源分类信息、评论数据、问答数据、收藏记录以及浏览和下载日志等。数据库的字符集统一设置为utf8mb4，以支持中文字符和特殊字符的正确存储。系统使用MyBatis-Plus作为ORM框架与MySQL进行交互，MyBatis-Plus在MyBatis的基础上做了增强，提供了代码生成器、分页插件、条件构造器等实用功能，能够有效减少重复的数据访问代码的编写量。')

h2('2.3 Redis缓存技术')
bt('Redis是一个基于内存的高性能键值对数据库，它支持字符串、哈希、列表、集合等多种数据结构。由于数据存储在内存中，Redis的读写速度非常快，单机每秒可以处理数十万次读写操作，因此被广泛用作缓存系统。Redis还支持数据的持久化，可以将内存中的数据定期保存到磁盘上，防止服务重启后数据丢失。')
bt('在本系统中，Redis主要用于缓存热点资源数据。当用户频繁访问某个教学资源的详情信息时，系统会先尝试从Redis缓存中读取，如果缓存命中则直接返回，避免了每次都查询数据库带来的性能开销；如果缓存未命中，则从MySQL数据库中查询数据，返回给用户的同时将数据写入Redis缓存，并设置合理的过期时间。当资源数据发生变更时，系统会主动清除对应的缓存，保证数据的一致性。此外，系统还对Redis的操作做了容错处理，当Redis服务不可用时，系统能够自动降级为直接查询数据库，确保核心功能不受影响。')

h2('2.4 MinIO文件存储技术')
